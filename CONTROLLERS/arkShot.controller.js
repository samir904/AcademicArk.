// 1️⃣1️⃣ END SESSION
//     PATCH /api/v1/arkshots/session/:sessionId/end
//     Body: { dropOffReason, isCompleted }
// ─────────────────────────────────────────────────────────────────────────────
export const endSession = async (req, res, next) => {
  try {
    const userId        = req.user?.id || req.user?._id;
    const { sessionId } = req.params;

    // ✅ For beacon calls — data comes from query params (no body)
    // ✅ For normal navigation calls — data comes from req.body
    const dropOffReason = req.body?.dropOffReason || req.query?.dropOffReason || 'navigated_away';
    const isCompleted   = req.body?.isCompleted   ?? req.query?.isCompleted   ?? false;

    if (!userId) return next(new AppError('Unauthorized', 401));

    const session = await ArkShotSession.findOneAndUpdate(
      { sessionId, user: userId },
      {
        $set: {
          endedAt:      new Date(),
          dropOffReason,
          isCompleted:  isCompleted === 'true' || isCompleted === true,
        },
      },
      { new: true }
    );

    if (!session) {
      return res.status(200).json({ success: true, message: 'Session not found or already ended' });
    }

    await session.save();
// CONTROLLERS/arkShot.controller.js — inside endSession, after session.save()

// ✅ Update collection avgSessionSeconds if this was a collection session
if (session.context?.collectionId && session.totalDurationSeconds > 0) {
  // Rolling average: newAvg = (oldAvg * (opens - 1) + newDuration) / opens
  const col = await ArkShotCollection.findById(
    session.context.collectionId,
    { 'stats.totalOpens': 1, 'stats.avgSessionSeconds': 1 }
  ).lean();

  if (col) {
    const opens  = col.stats.totalOpens || 1;
    const oldAvg = col.stats.avgSessionSeconds || 0;
    const newAvg = Math.round(
      (oldAvg * (opens - 1) + session.totalDurationSeconds) / opens
    );

    await ArkShotCollection.findByIdAndUpdate(
      session.context.collectionId,
      { $set: { 'stats.avgSessionSeconds': newAvg } }
    ).catch(() => {});
  }
}

    return res.status(200).json({
      success:              true,
      message:              'Session ended',
      totalDurationSeconds: session.totalDurationSeconds,
      totalShotsViewed:     session.totalShotsViewed,
    });

  } catch (err) {
    return next(new AppError(err.message, 500));
  }
};



