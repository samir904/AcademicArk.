// 6️⃣  RECORD VIEW
//     POST /api/v1/arkshots/:id/view
//     Body: { timeSpentSeconds, swipeDirection, arkShotSessionId }
// ─────────────────────────────────────────────────────────────────────────────
// ─────────────────────────────────────────────────────────────────────────────
// 6️⃣  RECORD VIEW
//     POST /api/v1/arkshots/:id/view
// ─────────────────────────────────────────────────────────────────────────────
// CONTROLLERS/arkShot.controller.js
export const recordView = async (req, res, next) => {
  try {
    const { id }   = req.params;
    const userId   = req.user.id;
    const {
      timeSpentSeconds    = 0,
      swipeDirection      = null,
      arkShotSessionId    = null,
      viewMode            = 'snap',
      isReview            = false,
      action              = 'seen',   // ✅ 'seen' | 'opened'
      scrollVelocityMs    = null,
      pauses              = [],
      totalPauseSeconds   = 0,
      pauseCount          = 0,
      hesitationCount     = 0,
      readDepthPercent    = 0,
      expandedDefinition  = false,
      openedDiagram       = false,
    } = req.body;

    console.log("record view body",req.body)
    // ✅ For 'opened' action — card was already counted when scrolled past.
    // We only push the intent signal to shotsViewed, skip all counters.
    const isOpenedAction = action === 'opened';

    const existing = await ArkShotProgress.findOne(
      { user: userId, arkShot: id },
      { _id: 1 }
    ).lean();

    const isFirstView = !existing;

    res.status(200).json({ success: true, message: 'View recorded' });

    const statsKey    = viewMode === 'snap' ? 'snapStats' : 'listStats';
    const isFastSwipe = timeSpentSeconds < 1;
    const isDeepRead  = readDepthPercent >= 80;

    // ── Background writes ─────────────────────────────────────────────
    // ✅ 'opened' skips global counters — card was already counted on scroll
    const writes = isOpenedAction ? [] : [
      ArkShot.findByIdAndUpdate(id, {
        $inc: {
          views: 1,
          ...(isFirstView && { uniqueViews: 1 }),
        },
      }),
      ArkShotProgress.findOneAndUpdate(
        { user: userId, arkShot: id },
        {
          $inc: { viewCount: 1, timeSpentSeconds },
          $set: {
            lastViewedAt:       new Date(),
            lastSwipeDirection: swipeDirection,
            ...(isFirstView && { firstViewedAt: new Date() }),
          },
        },
        { upsert: true }
      ),
      ArkShotAnalytics.findOneAndUpdate(
        { arkShot: id },
        { $inc: { totalViews: 1 } },
        { upsert: true }
      ),
    ];

    // ── Session update ────────────────────────────────────────────────
    if (arkShotSessionId) {
      writes.push(
        ArkShotSession.findOneAndUpdate(
          { sessionId: arkShotSessionId, user: userId },
          {
            $push: {
              shotsViewed: {
                arkShot:            id,
                viewedAt:           new Date(),
                timeSpentSeconds,
                action,             // ✅ dynamic — 'seen' or 'opened'
                isReview,
                scrollVelocityMs,
                pauses,
                totalPauseSeconds,
                pauseCount,
                hesitationCount,
                readDepthPercent,
                expandedDefinition,
                openedDiagram,
              },
            },

            // ✅ 'opened' only pushes the entry — no counter increments
            ...(!isOpenedAction && {
              $inc: {
                totalShotsViewed:                      1,
                [`${statsKey}.shotsViewed`]:            1,
                [`${statsKey}.totalTimeSeconds`]:       timeSpentSeconds,
                ...(!isReview && { uniqueShotsViewed: 1 }),
                ...(hesitationCount > 0 && {
                  'scrollBehaviour.totalHesitations': hesitationCount,
                }),
                ...(isFastSwipe && { 'scrollBehaviour.fastSwipeCount': 1 }),
                ...(isDeepRead  && { 'scrollBehaviour.deepReadCount':  1 }),
              },
              $set: { lastShotSeen: id },
            }),
          },
          { new: true, lean: true }
        ).then(sess => {
          if (!sess || isOpenedAction) return; // ✅ skip avg recalc for 'opened'

          const allViewed = sess.shotsViewed?.filter(s => s.action === 'seen') ?? [];
          const count     = allViewed.length;
          if (count === 0) return;

          const velocities = allViewed
            .map(s => s.scrollVelocityMs)
            .filter(v => v !== null && v !== undefined);

          const avgVelocityMs = velocities.length
            ? Math.round(velocities.reduce((a, b) => a + b, 0) / velocities.length)
            : null;

          const avgPauseSeconds = parseFloat(
            (allViewed.reduce((a, s) => a + (s.totalPauseSeconds ?? 0), 0) / count)
            .toFixed(2)
          );

          const avgReadDepthPercent = parseFloat(
            (allViewed.reduce((a, s) => a + (s.readDepthPercent ?? 0), 0) / count)
            .toFixed(1)
          );

          const avgUpdate = {};
          if (avgVelocityMs       !== null) avgUpdate['scrollBehaviour.avgVelocityMs']       = avgVelocityMs;
          if (avgPauseSeconds     !== null) avgUpdate['scrollBehaviour.avgPauseSeconds']     = avgPauseSeconds;
          if (avgReadDepthPercent !== null) avgUpdate['scrollBehaviour.avgReadDepthPercent'] = avgReadDepthPercent;

          const promises = [];

          if (Object.keys(avgUpdate).length) {
            promises.push(
              ArkShotSession.findOneAndUpdate(
                { sessionId: arkShotSessionId, user: userId },
                { $set: avgUpdate }
              ).catch(() => {})
            );
          }

          if (sess?.context?.collectionId) {
            promises.push(
              ArkShotCollection.findByIdAndUpdate(
                sess.context.collectionId,
                { $inc: { 'stats.shotsViewed': 1 } }
              ).catch(() => {})
            );
          }

          return Promise.all(promises);
        }).catch(() => {})
      );
    }

    Promise.all(writes).catch(err =>
      console.error('[recordView] background writes failed:', err)
    );

  } catch (err) {
    return next(new AppError(err.message, 500));
  }
};



// ─────────────────────────────────────────────────────────────────────────────
// 7️⃣  TOGGLE LIKE
// ─────────────────────────────────────────────────────────────────────────────
export const toggleLike = async (req, res, next) => {
  try {
    const { id }  = req.params;
    const userId  = req.user.id;
    const { arkShotSessionId = null } = req.body;

    // ✅ 1 lean read — only fetch what we need to compute response
    const existing = await ArkShotProgress.findOne(
      { user: userId, arkShot: id },
      { isLiked: 1 }
    ).lean();

    const nowLiked = existing ? !existing.isLiked : true;

    // ✅ Respond immediately — client has everything it needs
    res.status(200).json({
      success: true,
      isLiked: nowLiked,
      message: nowLiked ? 'Liked ❤️' : 'Unliked',
    });

    // ✅ All writes fire IN PARALLEL after response — never block client
    const writes = [
      // Merged 2 ArkShotProgress ops → 1
      ArkShotProgress.findOneAndUpdate(
        { user: userId, arkShot: id },
        { $set: { isLiked: nowLiked, likedAt: nowLiked ? new Date() : null } },
        { upsert: true }
      ),
      ArkShot.findByIdAndUpdate(id, {
        $inc: { likes: nowLiked ? 1 : -1 },
      }),
      ArkShotAnalytics.findOneAndUpdate(
        { arkShot: id },
        { $inc: { totalLikes: nowLiked ? 1 : -1 } },
        { upsert: true }
      ),
    ];

    if (arkShotSessionId) {
      writes.push(
        // ✅ Combine session update + collectionId fetch in ONE query (was 2)
        ArkShotSession.findOneAndUpdate(
          { sessionId: arkShotSessionId, user: userId },
          nowLiked ? { $inc: { totalLiked: 1 } } : {},
          { new: true, lean: true, projection: { 'context.collectionId': 1 } }
        ).then(sess => {
          if (sess?.context?.collectionId) {
            return ArkShotCollection.findByIdAndUpdate(
              sess.context.collectionId,
              { $inc: { 'stats.totalLikes': nowLiked ? 1 : -1 } }
            );
          }
        }).catch(() => {})
      );
    }

    Promise.all(writes).catch(err =>
      console.error('[toggleLike] background writes failed:', err)
    );

  } catch (err) {
    return next(new AppError(err.message, 500));
  }
};


