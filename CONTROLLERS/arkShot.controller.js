// TRACK COLLECTION OPEN
// PATCH /api/v1/arkshots/collections/:collectionId/track-open
// Called when user opens collection detail page
// Increments totalOpens + uniqueOpens on ArkShotCollection
// ─────────────────────────────────────────────────────────────────────────────
export const trackCollectionOpen = async (req, res, next) => {
  try {
    // ✅ Respond immediately — process async below
    res.status(200).json({ success: true });

    const { collectionId } = req.params;
    const userId           = req.user?.id;

    if (!collectionId) return;

    // ── Check if this user has opened this collection before ──
    const alreadyOpened = await ArkShotSession.exists({
      user:                   userId,
      'context.collectionId': collectionId,
    });

    const update = {
      $inc: { 'stats.totalOpens': 1 },
      $set: { lastOpenedAt: new Date() },
    };

    // ✅ Only count unique once per user
    if (!alreadyOpened) {
      update.$inc['stats.uniqueOpens'] = 1;
    }

    await ArkShotCollection.findByIdAndUpdate(
      collectionId,
      update
    ).catch(() => {});

  } catch (err) {
    // Silent — analytics never throws to user
  }
};


